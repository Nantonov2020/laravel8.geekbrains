    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->only(['name','email','message','phone']);
        $saveFile = function(array $data) {
            $responseData = [];
            $fileNews = storage_path('app/orders.txt');
            if(file_exists($fileNews)) {
                $file = file_get_contents($fileNews);
                $response = json_decode($file, true);
            }

            $responseData[] = $data;
            if(isset($response) && !empty($response)) {
                $r = array_merge($response, $responseData);
            }else {
                $r = $responseData;
            }
            file_put_contents($fileNews, json_encode($r));
        };
        $saveFile($data);

        return redirect()->route('orders.create')->with('success', 'Ваш заказ принят.');
    }

